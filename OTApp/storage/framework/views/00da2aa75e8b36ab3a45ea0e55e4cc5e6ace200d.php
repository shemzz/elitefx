      messaging.getToken()
        .then(function (currentToken) {
          if (currentToken) {
            sendTokenToServer(currentToken);
          } else {
            requestPermission();
            setTokenSentToServer(false);
          }
        })
        .catch(function (err) {
          setTokenSentToServer(false);
        });
