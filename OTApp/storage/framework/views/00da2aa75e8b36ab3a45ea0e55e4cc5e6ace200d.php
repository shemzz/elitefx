    function sendTokenToServer(currentToken) {
      if (!isTokenSentToServer()) {
        $.post(FCM_DEVICE_CREATE_URL, { registration_id: currentToken, csrfmiddlewaretoken: CSRFTOKEN })
          .always(function () {
            setTokenSentToServer(true);
          });
      }
    }

